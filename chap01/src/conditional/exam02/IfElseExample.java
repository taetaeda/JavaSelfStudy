package conditional.exam02;

public class IfElseExample {

	public static void main(String[] args) {
		
		int score = 85;
		
		if(score>=90) {
			System.out.println("점수가 90보다 큽니다.");
			System.out.println("등급은 a");
		} else {
			System.out.println("점수가 90보다 작아요");
			System.out.println("등급 b");
		}

	}

}
